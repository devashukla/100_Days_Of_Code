            # Assignment Operators
# Assignment operators are used to assign values to variables. The most common assignment operator is the equal
  # = , += , -= , *= , /= , //= , %= , **= etc.

x = 10 # = is an assignment operator that assigns the value 10 to the variable x.
print(x) # O/P => 10

y = 5 # Assign the value 5 to the variable y.
y += 5 # This is equivalent to y = y + 5. It adds 5 to the current value of y and assigns the result back to y.
print(y) # O/P => 10

z = 20 # Assign the value 20 to the variable z.
z -= 10 # Equivalent to z = z - 10. It subtracts 10 from the current value of z and assigns the result back to z.
print(z) # O/P => 10

a = 4 # Assign the value 4 to the variable a.
a *= 3 # Equivalent to a = a * 3. It multiplies the current value of a by 3 and assigns the result back to a.
print(a) # O/P => 12   

b = 10 # Assign the value 10 to the variable b.
b /= 2 #  Equivalent to b = b / 2. It divides the current value of b by 2 and assigns the result back to b.
print(b) # O/P => 5.0   

c = 10 # Assign the value 10 to the variable c.
c //= 3 # Equivalent to c = c // 3. It performs floor division on the current value of c by 3 and assigns the result back to c.
print(c) # O/P => 3

d = 10 # Assign the value 10 to the variable d.
d %= 3 # Equivalent to d = d % 3. It calculates the remainder of the division of the current value of d by 3 and assigns the result back to d.
print(d) # O/P => 1

e = 2 # Assign the value 2 to the variable e.
e **= 3 # Equivalent to e = e ** 3. It raises the current value of e to the power of 3 and assigns the result back to e.
print(e) # O/P => 8 
