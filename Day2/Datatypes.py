# Primitive datatypes

# Int
a = 10
print(a, type(a))

# Float
b = 3.12
print(b, type(b))

# String
c = "Hello World"
print(c[7]) # String is a collection of characters and we can access each character by its index. Index starts from 0.

a = "10"
b = "345"
print(a + b) # String concatenation // It will not add the numbers but will concatenate the strings.
# O/P => 10345

# Boolean
d = True
print(d, type(d))