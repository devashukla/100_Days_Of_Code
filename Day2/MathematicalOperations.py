# Basic Mathematical Operations => PEMDAS - Parentheses, Exponents, Multiplication, Division, Addition, Subtraction

# Addition
print(2 + 3)  # Output: 5
# Subtraction 
print(5 - 2)  # Output: 3
# Multiplication
print(4 * 3)  # Output: 12
# Division
print(10 / 2)  # Output: 5.0 => Always return float value.
# Exponentiation
print(2 ** 3)  # Output: 8
# Modulo
print(10 % 3)  # Output: 1
# Floor Division
print(10 // 3)  # Output: 3 => We use floor division to get the integer value as an output.
# PEMDAS
print(2 + 3 * 4)  # Output: 14 (Multiplication is performed before addition)
print((2 + 3) * 4)  # Output: 20 (Parentheses are evaluated first)